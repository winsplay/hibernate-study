package com.winsplay.hibernate.bean;

import java.util.Date;

public class Event {

	private long evId;
	private String title;
	private Date dt;
	
	
	public long getEvId() {
		return evId;
	}
	public void setEvId(long evId) {
		this.evId = evId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Date getDt() {
		return dt;
	}
	public void setDt(Date dt) {
		this.dt = dt;
	}
	
	
	
	
	
	
}

